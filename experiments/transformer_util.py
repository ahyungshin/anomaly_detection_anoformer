def entropy(p, dims = -1):
   return -torch.where(p > 0, p * p.log(), p.new([0.0])).sum(dim = dims, keepdim = False)

def _get_clones(module, N):
    return ModuleList([copy.deepcopy(module) for i in range(N)])

